# Arifmetik operatorlari

print(15 + 5)  # Qo'shish = Javobi: 20

print(15 - 5)  # Ayirish = Javobi: 10

print(5 * 5)  # Ko'paytirish = Javobi: 25

print(20 / 2)  # Bo'lish = Javobi: 10.0

print(6 % 2)  # Ex-1: Qoldiq = Javobi: 0  "  3 * 3 = 6 "
print(7 % 2)  # Ex-2: Qoldiq = Javobi: 1

print(5 ** 2)  # Ex-1: Kvadratli  Ko'paytirish = Javobi: 25 "  5 * 1 * 5  "
print(2 ** 3)  # Ex-2: Kvadratli  Ko'paytirish = Javobi: 8 "  2 * 1 * 2 * 2  "

print(6 // 2)  # Ex-1: Butun Sonni Qaytaradi = Javobi: 3  "  6 / 2 = 3  "
print(9 // 2)  # Ex-2: Butun Sonni Qaytaradi = Javobi: 4  "  9 / 2 = 4.5  "

# Topshiriq operatorlari


c = 23  # O'zgaruvchilar
print(c)  # Tenglash = Javobi: 23

a = 2
c += a
print(c)  # Qo'shib Tenglash = Javobi: 25

b = 5
c -= b
print(c)  # Ayirib Tenglash = Javobi: 20

k = 2
c *= k
print(c)  # Ko'paytirib Tenglash = Javobi: 40

x = 4
c /= x
print(c)  # Ko'paytirib Tenglash = Javobi: 10.0

z = 2
c %= z
print(c)  # Qoldiq Tenglash = Javobi: 0.0

v = 2
b **= v
print(b)  # Qoldiq Tenglash = Javobi: 25

n = 2
b //= n
print(b)  # Butun Sonni Tenglash = Javobi: 12
